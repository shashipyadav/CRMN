package com.example.myapplication.projectdetails;

public class AmenitiesModel {
    private String id;
    private String amenitiesdesc;
    private String amenitiesimage;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAmenitiesdesc() {
        return amenitiesdesc;
    }

    public void setAmenitiesdesc(String amenitiesdesc) {
        this.amenitiesdesc = amenitiesdesc;
    }

    public String getAmenitiesimage() {
        return amenitiesimage;
    }

    public void setAmenitiesimage(String amenitiesimage) {
        this.amenitiesimage = amenitiesimage;
    }
}
