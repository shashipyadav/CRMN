package com.example.myapplication.projectdetails;

public class BrochureModel {
    private String id;
    private String brochuredesc;
    private String brochureimage;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBrochuredesc() {
        return brochuredesc;
    }

    public void setBrochuredesc(String brochuredesc) {
        this.brochuredesc = brochuredesc;
    }

    public String getBrochureimage() {
        return brochureimage;
    }

    public void setBrochureimage(String brochureimage) {
        this.brochureimage = brochureimage;
    }
}
